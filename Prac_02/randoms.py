import random
print(random.randint(5, 20))
# the smallest number that could've appeared is 5 and the largest 20
print(random.randrange(3, 10, 2))
# the smallest number is 3 and the largest is 9
# a 4 cannot appear as the range increases in intervals of 2 starting from 3 (so 3 or 5 but not 4)
print(random.uniform(2.5, 5.5))
# the smallest number is 2.5 and the largest is 5.5
print(random.randint(1, 100))




